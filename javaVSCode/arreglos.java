public class arreglos {
    /* 
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Un arreglo puede definirse como un grupo o una coleccion finita, homogenica y ordenada de elementos
                           //0,1,2,3 
        int [] listaNumeros={5,3,9,1}; // Declaracion de un arreglo de enteros
        for(int i=0;i<=3;i++) {
            System.out.println(listaNumeros[i]); // Imprime los elementos del arreglo 
        }
        
    }
}
